// bits/stdc++.h works in linux. 
// It loads most of the libraries of C++ required.
#include <bits/stdc++.h> 

using namespace std;

int main() {
	// Read the number of test cases.
	int t;
	cin>>t;
	while(t){
	   int n1,n2,sum=0;
	   cin>>n1;
	   cin>>n2;
	   sum = n1+n2;
	   cout<<sum<<endl;
	   t--;
	}
	

	return 0;
}
